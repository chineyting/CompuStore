DROP TABLE IF EXISTS Account CASCADE ;
DROP TABLE IF EXISTS Customer CASCADE;
CREATE TABLE Customer (id INTEGER unsigned not NULL AUTO_INCREMENT,firstName VARCHAR(30), lastName VARCHAR(30), cust_email VARCHAR(50),cust_phone VARCHAR(12),primary key(id));
CREATE TABLE Account (username varchar(50) not NULL, credit_cardnum VARCHAR(20), user_id INTEGER unsigned not NULL, password VARCHAR(20), primary key(username), foreign key (user_id) REFERENCES Customer (id));
DROP TABLE IF EXISTS Warehouse CASCADE;
DROP TABLE IF EXISTS Branch CASCADE;
CREATE TABLE Branch (b_id INTEGER not NULL AUTO_INCREMENT, branch_name VARCHAR(255), branch_add VARCHAR(255),primary key(b_id));
DROP TABLE IF EXISTS Product CASCADE;
CREATE TABLE Product (serial_num VARCHAR(50) not NULL, product_name VARCHAR(50), product_brand VARCHAR(50), product_model VARCHAR(50), product_price INTEGER,primary key(serial_num));
CREATE TABLE Warehouse (serial_num VARCHAR(50) references Product, b_id INTEGER references Branch, quantity INTEGER, primary key(serial_num,b_id), foreign key (b_id) REFERENCES Branch(b_id), foreign key (serial_num) REFERENCES Product(serial_num));
